/**Por que usar exceções?
 * 
 * Quando algum erro acontece durante a execução de alguma função específica, é essencial que se saiba onde está o erro (qual função?).
 * Além disso, é fundamental que o programa já saiba o que fazer quando o problema acontecer, em vez de simplesmente crashar ou permanecer estático.
 * 
 * Para isso servem as exceções: para transmitir a origem do erro, a causa do erro e sua identificação, permitindo a sua correta solução.
 */

/**Erros:
 * 
 * São problemas que ocorrem durante execução do programa, mas estão fora do escopo do fluxo das regras do negócio. 
 * Exemplos:
 * 
 * StackOverflowError
 * OutOfMemoryError
 * ArgumentError
 * TypeError 
 */

